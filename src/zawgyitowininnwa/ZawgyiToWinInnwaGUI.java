    public void fileFilterType ()
    {
        fileChooser = new JFileChooser(); 
        // Creating a extension filters
        FileNameExtensionFilter docxExtensionFilter = new FileNameExtensionFilter("*.docx", "docx");
        FileNameExtensionFilter docExtensionFilter = new FileNameExtensionFilter("*.doc", "doc");
        
        fileChooser.setFileFilter(docExtensionFilter);
        fileChooser.setFileFilter(docxExtensionFilter);
        
    }// end fileFilterType ()
    
    public String ReadDoc () throws FileNotFoundException, IOException
    {
        // System.out.println("ReadDOC ***************");
        
        HWPFDocument doc = new HWPFDocument(new FileInputStream (fileChooser.getSelectedFile()));
        WordExtractor extractorDoc = new WordExtractor(doc);
        // System.out.println( extractorDoc.getText()); 
        
        String word = extractorDoc.getText(); 
        // System.out.println(word);
        
        return word; 
    }
    
    public String ReadDocx () throws FileNotFoundException, IOException
    {
        XWPFDocument docx = new XWPFDocument(new FileInputStream (fileChooser.getSelectedFile()));
        XWPFWordExtractor extractor = new XWPFWordExtractor(docx);
        
        
        return extractor.getText();
    }
    
    
    
