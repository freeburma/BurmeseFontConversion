        try
        {
            
            fileFilterType ();
            // fileChooser = new JFileChooser();
            
            int value = fileChooser.showOpenDialog(null);

            // Reading a *.docx file 
            if (value == JFileChooser.APPROVE_OPTION)
            {
                // Getting a document file
                String fileType = fileChooser.getFileFilter().getDescription(); 
                System.out.println(">> " + fileType);
                
                if (fileType.equals("*.doc"))
                {
                    System.out.println(">>>" + ReadDoc());
                    zwToInnwa.zawgyiToWinInnwa(ReadDoc());
                }
                
                if (fileType.equals("*.docx"))
                {
                    // Calling the ZawgyiTOWinInnawa Instance 
                    zwToInnwa.zawgyiToWinInnwa(ReadDocx());
                    
                }
                
                

            }// end if
        }
        catch (Exception e)
        {

        }
